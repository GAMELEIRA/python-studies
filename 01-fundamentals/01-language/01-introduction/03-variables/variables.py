# E POSSIVEL INFERIR O TIPO DE UMA VARIÁVEL UTILIZANDO A FUNCAO DE CONVERSAO DE TIPOS
x = str(3) # str CONVERTE PARA STRING  
y = int(3) # int CONVERTE PARA INTEIRO
z = float(3) # float CONVERTE PARA FLOAT

# A FUNCAO TYPE RETORNA O TIPO DA VARIÁVEL
print(type(x))  # <class 'str'>
print(type(y))  # <class 'int'>   
print(type(z))  # <class 'float'>