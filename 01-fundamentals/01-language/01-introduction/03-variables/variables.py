# VARIAVEIS PODEM SER REATRIBUÍDAS A QUALQUER MOMENTO 
# E NÃO É NECESSÁRIO DECLARAR O TIPO DA VARIÁVEL
# O TIPO DA VARIAVEL É DETERMINADO AUTOMATICAMENTE PELO INTERPRETADOR
# O PYTHON É UMA LINGUAGEM DE TIPAGEM DINÂMICA E FORTEMENTE TIPOADA
# O QUE SIGNIFICA QUE O TIPO DA VARIÁVEL PODE MUDAR DURANTE A EXECUCAO DO PROGRAMA E NAO E NECESSARIO DECLARAR O TIPO DA VARIAVEL
